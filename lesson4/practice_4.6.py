from random import random, randint
from itertools import count, cycle

# а) итератор, генерирующий целые числа, начиная с указанного
# выводим целые числа, начиная с 3, а при достижении числа 10 завершаем цикл.
for el in count(3):
    if el > 10:
        break
    else:
        print(el)

