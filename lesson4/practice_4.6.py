# б) итератор, повторяющий элементы некоторого списка, определенного заранее.
# также необходимо предусмотреть условие, при котором повторение элементов списка будет прекращено.

rand_list = [2, 7, 23]
с = 1
for el in cycle(rand_list):
    if с > 10:
        break
    print(el)
    с += 1

